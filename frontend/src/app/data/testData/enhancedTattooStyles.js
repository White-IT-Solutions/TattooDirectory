// Get all unique characteristics for filtering
export const getAllCharacteristics = () => {
  const characteristics = new Set();
  Object.values(enhancedTattooStyles).forEach(style => {
    style.characteristics.forEach(char => characteristics.add(char));
  });
  return Array.from(characteristics).sort();
};

// Get all unique motifs for inspiration
export const getAllMotifs = () => {
  const motifs = new Set();
  Object.values(enhancedTattooStyles).forEach(style => {
    style.popularMotifs.forEach(motif => motifs.add(motif));
  });
  return Array.from(motifs).sort();
};

// Search function for style aliases
export const searchStylesByAlias = (query) => {
  const lowercaseQuery = query.toLowerCase();
  return Object.values(enhancedTattooStyles).filter(style => 
    style.name.toLowerCase().includes(lowercaseQuery) ||
    style.aliases.some(alias => alias.toLowerCase().includes(lowercaseQuery)) ||
    style.description.toLowerCase().includes(lowercaseQuery)
  );
};

// Get popularity ranking
export const getStylesByPopularity = () => {
  return Object.values(enhancedTattooStyles)
    .sort((a, b) => b.popularity - a.popularity);
};